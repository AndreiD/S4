package com.ontotext.s4.classifier;

public class Score{
	private String label;
	private float score;
	public Score(){
		
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	
}
